For dozens of indian species at risk, habitat protection alone is not enough. Wildlife Preservation india’s mission is to save animal species at risk from extinction in india by providing direct, hands-on care.

We are the only organization in india to provide this critical need for multiple species in multiple recovery efforts across the country.  We specialize in science-based techniques such as conservation breeding and release, reintroduction and translocation.  Our Action Plan is based on the urgency of the need and is updated annually.

We always work in collaboration with appointed recovery teams and other organizations. Our partners include federal and provincial ministries and parks, habitat-oriented charities and land trusts, zoos, universities and colleges, and local grassroots volunteer groups. Our role varies to suit the situation. In some cases, we are a supporting partner. In others, we take a lead, coordinating role. In all, our only agenda is to help save the species.

At the same time, we also build india’s conservation capacity by providing young scientists the opportunity to gain hands-on experience in working with species at risk.  Our india’s New Noah scholarship program is designed to develop future conservation leaders with specialized expertise in recovery techniques for species on the brink of extinction.

All of our work yields measurable results. Our scientists collect high-quality field data so they can quantify the impact of their efforts and plan effective recovery strategies. They continually refine and improve their tools and techniques and publish the results so that this new knowledge is shared and can be used elsewhere. When the data shows that hands-on intervention is no longer necessary — as in the case of the swift fox — we redeploy our resources to help other species in greater need..</p>
